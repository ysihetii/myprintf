#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int l;

	l = ft_strlen(s1);
	i = l;
	while (s2[i - l])
	{
		s1[i] = s2[i - l];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
